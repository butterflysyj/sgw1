const generateExample = (word: { term: string; meaning: string; partOfSpeech: string }): { exampleSentence: string; exampleSentenceMeaning: string } => {
    const firstMeaning = word.meaning.split('/')[0];
    switch (word.partOfSpeech) {
        case '동사':
            return {
                exampleSentence: `I want to ${word.term}.`,
                exampleSentenceMeaning: `저는 ${firstMeaning} 싶어요.`
            };
        case '형용사':
            return {
                exampleSentence: `You are very ${word.term}.`,
                exampleSentenceMeaning: `당신은 매우 ${firstMeaning}.`
            };
        case '명사':
        default:
             const startsWithVowel = /^[aeiou]/i.test(word.term);
            return {
                exampleSentence: `This is ${startsWithVowel ? 'an' : 'a'} ${word.term}.`,
                exampleSentenceMeaning: `이것은 ${firstMeaning}입니다.`
            };
    }
};

const createWord = (id: number, korean: string, english: string, unit: number): Word => {
    const partOfSpeech = getPartOfSpeech(korean, english);
    const { exampleSentence, exampleSentenceMeaning } = generateExample({ term: english, meaning: korean, partOfSpeech });

    return {
        id,
        term: english,
        pronunciation: '',
        partOfSpeech,
        meaning: korean,
        exampleSentence,
        exampleSentenceMeaning,
        gradeLevel: 'middle2',
        isCustom: false,
        unit: unit,
    };
};

